// Type Conversions
// Most of the time operators automatically convert values given to them
// to the right type.
//
// String(45) -> "45" -> explicit type conversion
// 4 - '1' -> 3 -> implicit type conversion
// 4 + '4' -> "44" -> string addition
//
// Number("45") -> 45
// Number(undefined) -> NaN
// Number(null) -> 0
// Number(true) -> 1
// Number(false) -> 0
// NUmber("") -> 0
// true + 2 -> 3
//
//
// Boolean(1) -> true
// Boolean(0) -> false
// Boolean("") -> false
// Boolean("Jack") -> true
